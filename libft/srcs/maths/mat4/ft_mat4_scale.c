#include "libft.h"

t_mat4		ft_mat4_scale(t_vec3 s)
{
	return ((t_mat4)((t_mat4_data){
		s.x, 0, 0, 0,
		0, s.y, 0, 0,
		0, 0, s.z, 0,
		0, 0, 0, 1
	}));
}
