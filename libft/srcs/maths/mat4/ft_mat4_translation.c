#include "libft.h"

t_mat4		ft_mat4_translation(t_vec3 t)
{
	return ((t_mat4)((t_mat4_data) {
		1, 0, 0, t.x,
		0, 1, 0, t.y,
		0, 0, 1, t.z,
		0, 0, 0, 1
	}));
}
