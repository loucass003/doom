#include "libft.h"

t_vec2	ft_mat2_mulv(t_mat2 m, t_vec2 p)
{
	t_vec2	res;

	res.x = (p.x * m.a[0][0] + p.y * m.a[0][1]);
	res.y = (p.x * m.a[1][0] + p.y * m.a[1][1]);
	return (res);
}
