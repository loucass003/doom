#include <libft.h>

t_vec2		vec2_add_s(t_vec2 v, float s)
{
	v.x += s;
	v.y += s;
	return v;
}

t_vec2		vec2_sub_s(t_vec2 v, float s)
{
	v.x -= s;
	v.y -= s;
	return v;
}
